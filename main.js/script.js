const dataKey = document.querySelectorAll("[data-key]")

function setLanguage(lang) {
  dataKey.forEach(element => {
    const key = element.dataset.key
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }


  });
}



window.addEventListener("DOMContentLoaded", () => {
  const langButtons = document.querySelectorAll(".lang")

  const savedLang = localStorage.getItem("language") || "el";
  setLanguage(savedLang);

  langButtons.forEach(ele => {

    const btnLang = ele.textContent.toLowerCase()
    ele.classList.remove("active")
    if (btnLang === savedLang) {
      ele.classList.add("active")
    }
  })






  langButtons.forEach(item => {
    item.addEventListener("click", () => {
      const langs = item.textContent.toLowerCase()
      setLanguage(langs)
      localStorage.setItem("language", langs)


      langButtons.forEach(btn => {
        btn.classList.remove("active")
      })




      item.classList.add("active")


    })

  })
})